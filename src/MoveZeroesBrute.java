public class MoveZeroesBrute {
    public static void main(String[] args) {
        int[] nums = {0, 1, 0, 3, 12};              // ‚ûä –º–∞—Å—Å–∏–≤ —á–∏—Å–µ–ª

        moveZeroes(nums);                           // ‚ûã –≤—ã–∑—ã–≤–∞–µ–º –º–µ—Ç–æ–¥, —á—Ç–æ–±—ã –æ–±—Ä–∞–±–æ—Ç–∞—Ç—å –º–∞—Å—Å–∏–≤

        for (int n : nums) {                        // ‚ûå —Ü–∏–∫–ª –ø–æ –≤—Å–µ–º —á–∏—Å–ª–∞–º –º–∞—Å—Å–∏–≤–∞
            System.out.print(n + " ");              // ‚ûç –ø–µ—á–∞—Ç–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –≤ –∫–æ–Ω—Å–æ–ª—å
        }
    }

    public static void moveZeroes(int[] nums) {
        int[] result = new int[nums.length];        // ‚ûé —Å–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π –º–∞—Å—Å–∏–≤ —Ç–∞–∫–æ–≥–æ –∂–µ —Ä–∞–∑–º–µ—Ä–∞
        int index = 0;                              // ‚ûè —ç—Ç–æ –ø–æ–∑–∏—Ü–∏—è, –∫—É–¥–∞ –≤—Å—Ç–∞–≤–ª—è—Ç—å –Ω–µ–Ω—É–ª–∏

        for (int num : nums) {                      // ‚ûê —Ü–∏–∫–ª: –±–µ—Ä—ë–º –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç –∏–∑ nums
            if (num != 0) {                         // ‚ûë –µ—Å–ª–∏ —ç–ª–µ–º–µ–Ω—Ç –ù–ï –Ω–æ–ª—å
                result[index] = num;                // ‚ûí —Å–æ—Ö—Ä–∞–Ω—è–µ–º –µ–≥–æ –≤ result
                index++;                            // üîü —Å–¥–≤–∏–≥–∞–µ–º –ø–æ–∑–∏—Ü–∏—é –≤–ø–µ—Ä—ë–¥
            }
        }

        for (int i = 0; i < nums.length; i++) {     // ‚ì´ –∫–æ–ø–∏—Ä—É–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –æ–±—Ä–∞—Ç–Ω–æ –≤ nums
            nums[i] = result[i];
        }
    }
}
